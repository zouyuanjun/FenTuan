package com.lejiaokeji.fentuan.databean;

public class BannerImageList {
        String id;
        String shufflingFigure;
        String picture;

    public BannerImageList() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getShufflingFigure() {
        return shufflingFigure;
    }

    public void setShufflingFigure(String shufflingFigure) {
        this.shufflingFigure = shufflingFigure;
    }

    public String getPicture() {
        return picture;
    }

    public void setPicture(String picture) {
        this.picture = picture;
    }
}
